    Timer timer;

    private void carregacanal(String valor) {

        if (canal != "") {
            ControleRemotoObj.setPrecan((ControleRemotoObj.getNumcanal()));
        }

        if (canal.length() < 3) {
            canal = canal + valor.toString();

            if (timer != null) {
                timer.stop();

            }

            timer = new Timer(1000, new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent arg0) {
                    ControleRemotoObj.setNumcanal(Integer.parseInt(canal));
                    lbl_canal.setText("Canal " + canal);
                    lbl_canal.setVisible(true);
                    //    lbl_canal.setText(canal);
                    canal = "";

                }

            });

            timer.setRepeats(false);
            timer.start();

        }
    }
